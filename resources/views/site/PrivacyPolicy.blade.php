            سیاست حفظ حریم خصوصی
وب‌سایت وب‌جامه طبق قوانین جرایم رایانه‌ای متعهد می‌شود که اطلاعات کاربران را با تمام امکاناتی که در اختیار دارد محافظت کند و به هیچ‌عنوان این اطلاعات را در اختیار سازمان‌ها و مراکز دیگر به منظور تبلیغات یا خدمات دیگر ارائه ندهد.
همچنین ما این امکان را در اختیار کاربران قرار داده‌ایم تا کاربران در صورت عضویت در سایت وب‌جامه بتوانند، اطلاعات خود را ویرایش کنند یا در صورت درخواست برای حذف کاربری خود با ارسال درخواست به تیم پشتیبان این امکان برای آنها فراهم گردد.
ما چه اطلاعاتی را از کاربران جمع‌آوری می‌کنیم و چرا ؟
هنگام ورود شما به سایت اطلاعاتی همچون دفعات بازدید شما از سایت، مدت زمان حضور شما در سایت، نسخه مرورگر، دستگاهی که با آن سایت را مشاهده می‌کنید، منطقه جغرافیایی شما و اطلاعات فنی دیگر که جهت بهینه‌سازی و بررسی ترافیک سایت به ما کمک خواهد کرد. این اطلاعات به هیچ عنوان به سازمان‌های ثالث فروخته نخواهد شد و فقط جهت بهبود سایت وب‌جامه استفاده خواهد شد.
وقتی بازدیدکنندگان نظری در سایت ارسال می‌کنند، ما داده‌هایی که از کاربر هنگام نظر به جا مانده را جمع‌آوری می‌کنیم، مثل آدرس ایمیل، آدرس IP (نشانی پروتکل اینترنت)، زمان ارسال نظر و همچنین اگر آدرس ایمیل شما در سرویس که Gravatar ارائه می‌دهد ذخیره شده باشد، بعد از تائید نظر شما، تصویر کاربری شما در این سایت به نمایش در خواهد آمد.
