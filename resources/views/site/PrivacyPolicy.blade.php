اگر تصویری را در یکی از سرویس‌های ارائه شده در سایت وب‌جامه قرار می‌دهید باید توجه کنید که این تصویر حاوی داده‌هایی درباره‌ی موقعیت مکانی مشخص (EXIF GPS) نباشد. بازدیدکنندگان دیگر می‌توانند بعد از ذخیره سازی تصویر موقعیت مکانی این تصاویر را استخراج کنند.
کوکی‌ها
بعد از به اشتراک گذاشتن نظر در وبسایت، شما می توانید تصمیم بگیرید که نام، نشانی ایمیل و وبسایت‌تان در کوکی ذخیره بشود یا خیر. در این زمینه حق انتخاب دارید اما در صورت پذیرش ذخیره‌ی اطلاعات در کوکی، در مراجعات بعدی خود به وبسایت برای اشتراک‌گذاری نظرات، نیاز به پُر کردن فرم اطلاعات نام‌برده نخواهید داشت. ماندگاری کوکی‌ها در وبسایت به مدت 1 سال است.
اگر در وبسایت دارای حساب کاربری باشید و با آن به وب وارد بشوید، ما اقدام به تنظیم کوکی موقت برای شما می‌کنیم تا مشخص شود که مرورگر شما پذیرای کوکی‌ها هست یا خیر. این کوکی شامل هیچ داده‌ی شخصی و خصوصی نیست و با بستن مرورگرتان متوقف می‌شود.
بعد از اینکه به حساب کاربری خود در وبسایت وارد می‌شوید، چندین کوکی برای ذخیره‌ی اطلاعات ورودتان و انتخاب‌هایی که در صفحه‌ی وبسایت صورت داده‌اید، نصب و تنظیم می‌شوند. کوکی‌های ورودی به مدت 7 روز فعال هستند و اطلاعات انتخاب صفحاتی که داشته‌اید نیز در طول 1 سال در دسترس قرار خواهند داشت. اگر گزینه «من را به خاطر بسپار» را انتخاب کنید، در طول 2 هفته آینده، ورود شما بدون نیاز به وارد کردن مجدد نام کاربری و کلمه عبور خواهد بود. در صورتی که از حساب کاربری خود خارج بشوید، کوکی‌های ورود از بین می‌روند.
وب‌سایت وب‌جامه از این کوکی‌ها تنها به منظور ایجاد تجربه کاربری بهتر برای بازدیدکنندگان خود استفاده می‌کند و هیچ‌گونه استفاده تبلیغاتی از این کوکی‌ها در نظر گرفته نخواهد شد.
محتوا و منابع استفاده شده از سایتی دیگر
مقالات و محتواهای سایت وب‌جامه ممکن است شامل منابع متفاوتی ( ویدئو، تصویر، متن و… ) باشد. اگر محتوایی از سایت دیگر در وب‌جامه قرار بگیرد، عیناً در قالب و شکلی که در آن سایت دیده شده، منتشر خواهد شد. ممکن است این وب‌سایت‌ها اطلاعات کاربران را دریافت کنند که برای مشاهده این موارد بهتر است قبل از استفاده از این سایت‌ها به برگه سیاست حفظ حریم خصوصی کاربران این سایت‌ها مراجعه کنید.
تا چه مدت اطلاعات شما را نگهداری می‌کنیم ؟
اگر در وبسایت ما نظر خود را به اشتراک بگذارید، نظر و داده‌های متای آن به مدت نامحدود نگهداری و ذخیره خواهد شد. در این صورت امکان پیگیری نظرات به‌صورت خودکار مقدور می‌شود و نیازی به نگه داشتن نظراتِ در انتظار بررسی نخواهد بود.
کاربرانی که در وبسایت ما ثبت‌نام می‌کنند، اطلاعات کاربری‌شان نیز حفظ و ذخیره می‌شود. تمام کاربران می‌توانند اطلاعات خود را مشاهده کنند، آنها را ویرایش کنند یا اطلاعات شخصی خود را هر وقت که مایل باشند، پاک کنند. (فقط امکان تغییر نام کاربری وجود ندارد). مدیران وبسایت نیز می‌توانند این اطلاعات را رؤیت و ویرایش کنند.
دیدگاه‌های بازدیدکننده ممکن است از طریق یک سرویس تشخیص هرزنامه خودکار بررسی شوند.
تبلیغات
امکان دارد شرکت‌های تبلیغاتی که ما از آنها تبلیغات دریافت می‌کنیم سمت کاربر کوکی‌های را ارسال کنند که اطلاعاتی همچون تعداد دفعات نمایش تبلیغ به شما، موقعیت جغرافیایی شما را به این شرکت ارسال کنند، که این اقدام جهت بهبود در امر نمایش تبلیغ است. سایت وب‌جامه با شرکت‌های تبلیغات غیر معتبر که اطلاعات اضافه از کاربران دریافت می‌کند، همکاری نمی‌کند.
حقوق شما در رابطه با داده‌های خود چیست؟
اگر در این وبسایت، صاحب حساب کاربری باشید، یا نظری به اشتراک بگذارید، حق درخواست فایل داده‌های شخصی خود -یعنی داده‌هایی که ما از شما دریافت و نگهداری کرده‌ایم- را دارید. این داده‌ها شامل هر چیزی است که در وبسایت بر جای گذاشته‌اید. ضمنا حق درخواست برای پاک کردن تمام داده‌های شخصی‌تان نیز کاملا محفوظ است. البته این داده‌ها مواردی را که برای اهداف مدیریتی، قانونی و امنیتی در اختیار داریم، شامل نمی‌شود.
